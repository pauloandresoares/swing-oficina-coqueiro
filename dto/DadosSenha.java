package dto;

public class DadosSenha {
	private String senhaUsuario;
	private String senhaSenha;
	private String senhaNome;
	private String senhaRG;
	private String senhaCPF;
	/**
	 * @return the senhaCPF
	 */
	public String getSenhaCPF() {
		return senhaCPF;
	}
	/**
	 * @param senhaCPF the senhaCPF to set
	 */
	public void setSenhaCPF(String senhaCPF) {
		this.senhaCPF = senhaCPF;
	}
	/**
	 * @return the senhaNome
	 */
	public String getSenhaNome() {
		return senhaNome;
	}
	/**
	 * @param senhaNome the senhaNome to set
	 */
	public void setSenhaNome(String senhaNome) {
		this.senhaNome = senhaNome;
	}
	/**
	 * @return the senhaRG
	 */
	public String getSenhaRG() {
		return senhaRG;
	}
	/**
	 * @param senhaRG the senhaRG to set
	 */
	public void setSenhaRG(String senhaRG) {
		this.senhaRG = senhaRG;
	}
	/**
	 * @return the senhaSenha
	 */
	public String getSenhaSenha() {
		return senhaSenha;
	}
	/**
	 * @param senhaSenha the senhaSenha to set
	 */
	public void setSenhaSenha(String senhaSenha) {
		this.senhaSenha = senhaSenha;
	}
	/**
	 * @return the senhaUsuario
	 */
	public String getSenhaUsuario() {
		return senhaUsuario;
	}
	/**
	 * @param senhaUsuario the senhaUsuario to set
	 */
	public void setSenhaUsuario(String senhaUsuario) {
		this.senhaUsuario = senhaUsuario;
	}
	
	
	
	
}
